package model;

import java.sql.Date;

/**
 *
 * @author Lehait
 */
public class Orderday {
    public int id;
    public Date day;
    public float price;

    public Orderday() {
    }

    public Orderday(int id, Date day, float price) {
        this.id = id;
        this.day = day;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getDay() {
        return day;
    }

    public void setDay(Date day) {
        this.day = day;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }
    
    
    
}
